package com.bariekly.javadicoding.praktek1;

public class OperatorAssingment {
    public static void main(String[] args){
        int value = 2;
        int anotherValue = 19;
        System.out.println("Nilai dari Value adalah =>> " + value);
        System.out.println("Nilai dari anotherValue adalah =>> " + anotherValue);
    }
    /* Operator Assingment disimbolkan dengan "=" yang menandakan nilai sebelah kanan (right operand)
    dimasukkan ke variable yang ada di sebelah kiri (left operand).

     */
}
