package com.bariekly.javadicoding.praktek1;

public class OperatorAritmetika {
    public static void main(String[]args){
        System.out.println("System operasi penjumlahan");
        int result = 5 +3;
        System.out.println("Hasil penjumlahan 5 + 3 adalah " + result);

        System.out.println("System operasi pengurangan");
        result = 5 - 3;
        System.out.println("Hasil pengurangan 5 - 3 adalah " + result);

        System.out.println("System operasi perkalian");
        result = 5 * 3;
        System.out.println("Hasil perkalian 5 * 3 adalah " + result);

        System.out.println("System operasi pembagian");
        result = 5 / 3;
        System.out.println("Hasil pembagian 5 / 3 adalah " + result);


    }
}
